    private void btn_timKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timKiemActionPerformed
        // TODO add your handling code here:
        if (txt_timKiem.getText().isEmpty() || txt_timKiem.getText().equals("Nhập MaDia")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã Đĩa");
        } else {
            DefaultTableModel model = (DefaultTableModel) jTable_donHang.getModel();
            model.setRowCount(0);
            for (ChiTietBanDia ee : dao.getAllDonHang()) {
                if ((ee.getMaDonHang().trim()).equals(txt_maDonHang.getText()) && (ee.getMaDia().trim()).equals(txt_timKiem.getText())) {

                    Object dataRow[] = new Object[5];
                    dataRow[0] = ee.getMaDia();
                    dataRow[1] = ee.getDonGia();
                    dataRow[2] = ee.getSoLuongMua();
                    dataRow[3] = ee.getGiamGia();
                    dataRow[4] = ee.getThanhTien();
                    model.addRow(dataRow);

                }

            }
            txt_timKiem.setText("");
        }
    }//GEN-LAST:event_btn_timKiemActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int x = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?", "Thêm", JOptionPane.YES_NO_OPTION);
        if (x == JOptionPane.YES_OPTION) {
            if (validateForm()) {
                ChiTietBanDia eeNew = getModel();
                try {
                    
                    if (kiemTraNhapSoLuongMua() == false) {
                        JOptionPane.showMessageDialog(this, "Số lượng đĩa cần mua > " + laySoLuongDiaTrongKho() + " đĩa còn trong kho", "Lỗi Kho", JOptionPane.ERROR_MESSAGE);
                    } else {
                        if (kiemTraNhapSoLuongMua()==true&&dao.updateTTBan(eeNew) > 0) {
                            JOptionPane.showMessageDialog(this, "Sửa thành công");
                            fillDatabase();
                            fillTongTien();
                        }
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error: " + e.toString());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin!!!");
            }
        }
    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int x = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Thêm", JOptionPane.YES_NO_OPTION);
        if (x == JOptionPane.YES_OPTION) {
            if (txt_maDia.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập Mã Đĩa");
            } else {
                try {
                    if (txt_maDia.getText().isEmpty() == false) {
                        if (dao.deleteTTBan(txt_maDonHang.getText(), txt_maDia.getText()) > 0) {
                            JOptionPane.showMessageDialog(this, "Xóa thành công");
                            fillDatabase();
                            fillTongTien();
                        }
                    }

                    resetForm();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error: " + e.toString());
                }
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

