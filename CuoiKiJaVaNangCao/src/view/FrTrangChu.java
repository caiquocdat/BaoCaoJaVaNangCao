    public void fillDatabase() {
        String[] arr = {"MaDia", "DonGia", "SoLuongMua", "GiamGia", "ThanhTien"};

        DefaultTableModel model = (DefaultTableModel) jTable_donHang.getModel();

        model.setRowCount(0);
        for (ChiTietBanDia ee : dao.getAllDonHang()) {
            if ((ee.getMaDonHang().trim()).equals(txt_maDonHang.getText())) {

                Object dataRow[] = new Object[5];
                dataRow[0] = ee.getMaDia();
                dataRow[1] = ee.getDonGia();
                dataRow[2] = ee.getSoLuongMua();
                dataRow[3] = ee.getGiamGia();
                dataRow[4] = ee.getThanhTien();
                model.addRow(dataRow);
            }

        }

    }

    //Add dữ liệu vào combobo
    public void fillCombobox() {
        for (ThongTinNhanVien ee : dao.LoadDataToCombobox()) {
            cb_maNhanVien.addItem(ee.getMaNV().toString());
        }
    }
    //Lấy giá trị hàng cuối cùng trong bảng HoaDonBan

    public void fillTextMaDonHang() {
        for (BanDia ee : dao.GiaTriHangCuoi()) {
            String cpChuoi = ee.getMaDonHang().substring(2).trim();
            int cpSo = Integer.valueOf(cpChuoi) +1 ;
            String maHD = "HD" + String.valueOf(cpSo);
            txt_maDonHang.setText(maHD);
        }
    }

    public void fillTongTien() {
        for (BanDia ee : dao.LoadDataToTongTien()) {
            if ((ee.getMaDonHang().trim()).equals(txt_maDonHang.getText())) {

                txt_tongTien.setText(String.valueOf(ee.getTongTien()));
            }
        }
    }

    public boolean kiemTraNhapSoLuongMua() {
        for (QuanLyKhoDia ee : daok.getAllDia()) {
            if ((ee.getMaDia().trim().equals(txt_maDia.getText().trim())) && (ee.getSoLuongCon() < Integer.valueOf(txt_soLuong.getText()))) {
                return false;
            }
        }
        return true;
    }

    public String laySoLuongDiaTrongKho() {
        for (QuanLyKhoDia ee : daok.getAllDia()) {
            if ((ee.getMaDia().trim().equals(txt_maDia.getText()))) {
                return String.valueOf(ee.getSoLuongCon());
            }
        }
        return "";
    }

