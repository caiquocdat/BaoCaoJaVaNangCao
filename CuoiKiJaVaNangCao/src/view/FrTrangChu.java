    public boolean validateFormThongTinChung() {
        if (txt_maKhachHang.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public BanDia getModelThongTinChung() {
        BanDia ee = new BanDia();
        ee.setMaDonHang(txt_maDonHang.getText());
        ee.setMaNV(cb_maNhanVien.getSelectedItem().toString());
        ee.setMaKH(txt_maKhachHang.getText());
        return ee;
    }
    private void btn_luuThongTinChungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuThongTinChungActionPerformed
        // TODO add your handling code here:
        int x = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không?", "Thêm", JOptionPane.YES_NO_OPTION);
        if (x == JOptionPane.YES_OPTION) {
            if (validateFormThongTinChung()) {
                BanDia ee = getModelThongTinChung();
                try {
                    if (dao.addTTChung(ee) > 0) {
                        fillDatabase();
                        JOptionPane.showMessageDialog(this, "Thêm thành công");
                        btn_luuThongTinChung.setEnabled(false);
                        txt_maKhachHang.setEnabled(false);
                        cb_maNhanVien.setEnabled(false);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error: " + e.toString());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng điền đủ thông tin!!!");
            }
        }

    }//GEN-LAST:event_btn_luuThongTinChungActionPerformed

